class ItemData{

  final String fileName;
  final String description;
  final List<String> tags;

  const ItemData(this.fileName, this.description, this.tags);

  bool matches(String text){

    text = remPolChars(text);

    if(remPolChars(description).contains(text)) return true;

    for(String tag in tags)
      if(remPolChars(tag).contains(text)) return true;

    return false;
  }

}
